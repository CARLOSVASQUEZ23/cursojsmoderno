// Métodos de string - indexOf, includes y lenght

//Método .lenght
const producto = "Monitores en oferta de LG";
console.log(producto);

//Método .lenght cuenta la cantidad de letras y espacios ocupados en un string
console.log(producto.length);

//Método .indexOf para realizar una búsqueda en mi cadena de texto devuelve la posición o -1
console.log(producto.indexOf('monitores'));

//Método .includes para realizar una búsqueda en mi cadena de texto, devuelve true o false
console.log(producto.includes('monitores'));

//El método .lenght es de los POCOS métodos que no utiliza paréntesis